int redPin = 12;
int bluePin = 8;
int greenPin = 5;
int yellowPin = 3;

void setup() {

pinMode(redPin,OUTPUT);
pinMode(bluePin,OUTPUT);
pinMode(greenPin,OUTPUT);
pinMode(yellowPin,OUTPUT);

}

void loop() {

// Red
digitalWrite(redPin,HIGH);

// Blue
digitalWrite(bluePin,HIGH);

// Green
digitalWrite(greenPin,HIGH);

// Yellow
digitalWrite(yellowPin,HIGH);

}
